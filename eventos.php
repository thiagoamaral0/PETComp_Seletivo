        <h1>EVENTOS</h1>
        <h3>Eventos serão promovidos e organizados pelo grupo. Entre as possíveis opções de eventos estão o EACOMP(Encontro Acadêmico de Computação), PET Game Jam, JIM.</h3>

        <h3>Os eventos serão planejados através de reuniões entre o grupo. Na primeira reunião, um petiano é eleito líder do monitoramento das atividades a serem realizadas para que ocorra o evento. Este líder, em conjunto com o tutor, indica membros para auxiliá-lo diretamente, podendo inclusive, ser todos os membros do PET. O tutor atua como facilitador entre as várias entidades e atores da universidade.</h3>

        <h3>O grupo então define a programação baseada em minicursos, tutorias, palestras, competições, apresentação, rodas de discussão e laboratórios e define também comissões para cuidar de grandes tarefas do evento. A programação é baseada na tendência atual da área e tende a atender os anseios do público diretamente envolvido do curso de Ciência da Computação.</h3>

        <h3>Os eventos são então divulgados através de redes sociais em todos os cursos da área do Maranhão, em instituições públicas e particulares, assim como em instituições que possuem cursos técnicos na área. Durante os eventos, as comissões formadas atuam para garantir que a realização do evento ocorra de forma completamente ordenada.</h3>
